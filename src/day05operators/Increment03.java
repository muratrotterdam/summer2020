package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		// int data tipinde iki tane variables olusturun...bu variableleri en kisa yolu kullanarak 1 arttirin.
		// en kisa yol sadece ve sadece 1 arttirmak icin kullanilir baska degerlerde calismaz
		
		int sayi1 = 12;
		sayi1++;				//variable isimden sonra 2 kere ++ yazilirsa o variable 1 artar
		System.out.println(sayi1);
		
		int sayi2=101;
		sayi2++;
		System.out.println(sayi2);
	}

}
